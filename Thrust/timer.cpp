#include <stddef.h>
#include <sys/time.h>

//////////////////////////////////////////////////////////////////////////////
///
/// @fn curr_second
///
/// @brief Basic timer function.
///
/// @return Double precision value representing time.
///
//////////////////////////////////////////////////////////////////////////////
double curr_second (void)
{
    struct timeval tv;
    gettimeofday(&tv, NULL);
    return (double)tv.tv_sec + (double)tv.tv_usec / 1000000.0;
}
